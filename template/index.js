function closeMenu(){
    document.getElementById("dropdown_content").style.display="none";
    document.getElementById("dropdown_togle").style.display="block";
    document.getElementById("dropdown_togleX").style.display="none";
}

function openMenu(){
    document.getElementById("dropdown_content").style.display="block";
    document.getElementById("dropdown_togle").style.display="none"
    document.getElementById("dropdown_togleX").style.display="block";
}


